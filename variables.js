// Variables
// JavaScript tiene 3 tipos de variables: String, Number y Boolean. Ademas tiene las variables null y undefined que expresan algo que no existe.
let nombre = 'Gabriel'
let esMayorDeEdad = true;

console.log('Antes',nombre, esMayorDeEdad);
// Para cambiar una variable solamente se coloca el nombre de la misma y el nuevo valor.
nombre = 34;

console.log('Despues',nombre, esMayorDeEdad);

// Constantes
const edad = 27;

edad = 31; // Esto dara errror en la consola. Ya que una constante no puede cambiar de valor.